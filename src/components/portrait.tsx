interface Props {
  animate?: boolean;
  maxLines?: number;
}

export default function Portrait({ animate, maxLines }: Props) {
  const [frame, setFrame] = useState(0);

  useEffect(() => {
    if (!animate) {
      setFrame(0);
      return;
    }
    const t = setInterval(() => setFrame((f) => (f === 0 ? 1 : 0)), 150);
    return () => clearInterval(t);
  }, [animate]);

  let rows: string[];
  if (animate) {
    rows = PORTRAIT.slice();
    const mouth = MOUTH_FRAMES[frame];
    for (let i = 0; i < mouth.length; i++) {
      rows[MOUTH_START + i] = mouth[i];
    }
  } else {
    rows = PORTRAIT;
  }

  const display = maxLines != null ? rows.slice(0, maxLines) : rows;

  return (
    <Box flexShrink={0}>
      <Text color="green">
        {display.join('\n')}
      </Text>
    </Box>
  );
}
