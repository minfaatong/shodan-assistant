const MOUTH_ROW = 25;
const MOUTH_COL_START = 22;
const MOUTH_COL_END = 26;

interface Props {
  animate?: boolean;
  maxLines?: number;
}

export default function Portrait({ animate, maxLines }: Props) {
  const [frame, setFrame] = useState(0);

  useEffect(() => {
    if (!animate) {
      setFrame(0);
      return;
    }
    const t = setInterval(() => setFrame((f) => (f === 0 ? 1 : 0)), 150);
    return () => clearInterval(t);
  }, [animate]);

  let rows: string[];
  if (animate && frame === 1) {
    rows = PORTRAIT.slice();
    const original = PORTRAIT[MOUTH_ROW];
    rows[MOUTH_ROW] =
      original.substring(0, MOUTH_COL_START) +
      ' '.repeat(MOUTH_COL_END - MOUTH_COL_START + 1) +
      original.substring(MOUTH_COL_END + 1);
  } else {
    rows = PORTRAIT;
  }

  const display = maxLines != null ? rows.slice(0, maxLines) : rows;
  const padded = ['', ...display.map((l) => ' ' + l), ''];

  return (
    <Box flexShrink={0}>
      <Text color="green" backgroundColor="#1c1c1c">
        {padded.join('\n')}
      </Text>
    </Box>
  );
}
